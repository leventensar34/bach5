package Days10_LogicalOperators;

public class Sifre {

    public static void main(String[] args) {

        System.out.println("Sifre sistemi");

        boolean sayi1=false, sayi2=false, sayi3 = false;
        //AND(&&) Logical operator
        sayi1 = true;
        sayi2 = true;
        sayi3 = true;


        System.out.println("Sifre... " + (sayi1 && sayi2 && sayi3));




    }
}
