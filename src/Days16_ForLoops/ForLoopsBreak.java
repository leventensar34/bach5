package Days16_ForLoops;

public class ForLoopsBreak {

    public static void main(String[] args) {

        System.out.println("For Loop Break");

        for (int i = 1; i < 10; i++) {
            System.out.println("i = " + i);

            if (i == 5) {
                System.out.println("For Loop tan Break sayesinde ciktim");
                break;
            }


        }


    }
}
