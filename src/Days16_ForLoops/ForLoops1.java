package Days16_ForLoops;

public class ForLoops1 {

    public static void main(String[] args) {

        for (int i = 1; i <=10; i++) {
            System.out.println(i+"-Merhaba");

        }
        for (int i =10; i >0 ; i--) {
            System.out.print(i+" ");
        }


        for (int i = 100; i >0 ; i--) {
            System.out.println(i);
        }





    }

}
