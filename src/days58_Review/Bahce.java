package days58_Review;

public class Bahce {

    int zemin;
    boolean cit;
    boolean mangal;

    public Bahce(int zemin, boolean cit, boolean mangal) {
        this.zemin = zemin;
        this.cit = cit;
        this.mangal = mangal;
    }

    public int getZemin() {
        return zemin;
    }

    public boolean isCit() {
        return cit;
    }

    public boolean isMangal() {
        return mangal;
    }
}
