package Days9_Unary_Assigment_Relation;

public class AssigmentOperators {

    public static void main(String[] args) {

        int i = 100;
        System.out.println("i = " + i);

        i += 1;
        System.out.println("i = " + i);

        i-=1;
        System.out.println("i = " + i);

        i*=2;
        System.out.println("i = " + i);

        i/=2;
        System.out.println("i = " + i);

        i%=5;
        System.out.println("i = " + i);



    }

}
