package days23;

public class passingArguement {

    public static void main(String[] args) {

        displayValue(5);

        int sayi = 10;

        displayValue(sayi);

        displayValue(5 * 3);

        displayValue(27 / 7);

    }

    public static void displayValue(int number) {
        System.out.println("Value is : " + number);

    }


}
