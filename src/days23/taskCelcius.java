package days23;

public class taskCelcius {

    public static void main(String[] args) {

        convertToFahreneit();

    }

    public static void convertToFahreneit() {

        double celcius = 32.6;
        double fahreneit = (9 * celcius / 5) + 32;
        System.out.println("fahreneit = " + fahreneit);

    }


}
